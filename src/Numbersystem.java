public class Numbersystem {
    static void main(String[] args) {

        //to represent a binary number "ob__"
        //to represent a decimal number "___"
        System.out.println(10); // decimal
        System.out.println(010); //octal
        System.out.println(0b10); //binary
        System.out.println(0x5a); //hexadecimal
        System.out.println(3e3); //to represent 10^ we use e

    }
}
