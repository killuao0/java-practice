package OPERATOR;

public class Relationaloperator {
    static void main(String[] args) {
        // relational operators always give answers in true or false
        System.out.println(10>9);
        System.out.println(10<9);
        System.out.println(10>=10);
        System.out.println(10>10);
        System.out.println(10>9);
        //! is used as not
        System.out.println(10!=9);
    }
}
